# Numerical types
# Intergers, Floats, complex numbers, big ints


# integers
# full numbers
print(10)
print(type(10))

# floats
# decimal numbers
print(10.0)
print(type(10.0))

# they can be used together
example = print(10/3)
print(type(example))

#they can be added, subratced, divided, multiplied

# and modulus (%)
# gives you remainder
print(10%2) # no remainder
print(10%3) # 3*3 is 9, one left over


# casting data types
# from string to integer
exmaple_var = '10'
print(type(example))

exmaple_var = int(exmaple_var)
print(type(exmaple_var))


